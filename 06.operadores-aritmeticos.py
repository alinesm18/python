# operadores aritmeticos

# soma 
soma = 10 + 5
print (soma)

# subtração 
subtracao = 10 - 5
print (subtracao)

# multiplicação 
multiplicacao = 10 * 5
print (multiplicacao)

# divisão 
divisao = 10 / 5
print (divisao)

# divisão inteira 
divisao_inteira = 10 // 5
print (divisao_inteira)

# exponenciação 
exponenciacao = 10 ** 5
print (exponenciacao)

# módulo / resto da divisão
modulo = 10 % 5
print (modulo)